#include "libft.h"

char		*ft_strrev(char *s)
{
	int		i;
	int		j;
	char	temp;

	j = 0;
	while (s[j] != '\0')
		j++;
	i = -1;
	while (++i < --j)
	{
		temp = s[i];
		s[i] = s[j];
		s[j] = temp;
	}
	return (s);
}
