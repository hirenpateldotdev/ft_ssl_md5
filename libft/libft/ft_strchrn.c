#include "libft.h"

int		ft_strchrn(const char *s, int c)
{
	int i;

	i = 0;
	while (*s != '\0' && *s != (char)c)
	{
		s++;
		i++;
	}
	if (*s != (char)c)
		return (-1);
	else
		return (i);
}
